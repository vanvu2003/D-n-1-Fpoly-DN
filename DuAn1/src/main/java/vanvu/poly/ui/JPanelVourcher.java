    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        Voucher vch = new Voucher();

        try {
            setVoucher(vch);
        } catch (ParseException ex) {
            Logger.getLogger(JPanelVourcher.class.getName()).log(Level.SEVERE, null, ex);
        }

        boolean update = vchDao.update(vch);
        if (update) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công voucher", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            list = (ArrayList<Voucher>) vchDao.fillAll();
            fillTable();
            clear();
            return;
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại!!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        Voucher vch = new Voucher();

        try {
            setVoucher(vch);
        } catch (ParseException ex) {
            ex.printStackTrace();
        }

        list.add(vch);

        boolean insert = vchDao.insert(vch);
        if (insert) {
            JOptionPane.showMessageDialog(this, "Thêm thành công voucher", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            list = (ArrayList<Voucher>) vchDao.fillAll();
            fillTable();
            clear();
            return;
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại!!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnThemActionPerformed

