    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String maVoucher = txtMaGiamGia.getText();

        int chose = JOptionPane.showConfirmDialog(this, "Bạn có chắn chắn muốn xóa không?", "Question", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (chose == JOptionPane.YES_OPTION) {
            boolean delete = vchDao.delete(maVoucher);
            if (delete) {
                JOptionPane.showMessageDialog(this, "Xóa thành công voucher", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                list = (ArrayList<Voucher>) vchDao.fillAll();
                fillTable();
                clear();
                return;
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại!!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblVoucherMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVoucherMouseClicked
        int row = tblVoucher.getSelectedRow();

        Voucher vch = list.get(row);
        setTextForm(vch);
        txtMaGiamGia.enable(false);
    }//GEN-LAST:event_tblVoucherMouseClicked

    public void fillTable() {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        DefaultTableModel model = (DefaultTableModel) tblVoucher.getModel();

        model.setRowCount(0);
        for (Voucher vc : list) {
            String ngayBD = sdf.format(vc.getNgayBD());
            String ngayKT = sdf.format(vc.getNgayKT());
            model.addRow(new Object[]{vc.getTenVoucher(), vc.getMaVoucher(), vc.getTriGia(), vc.getGiaToiThieu(), ngayBD, ngayKT, vc.getGhiChu()});
        }
        tblVoucher.setModel(model);
    }

    public void setVoucher(Voucher vch) throws ParseException {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String ngayBD = sdf.format(jdcNgayBD.getDate());
        String ngayKT = sdf.format(jdcNgayKT.getDate());

        vch.setMaVoucher(txtMaGiamGia.getText().toUpperCase());
        vch.setTenVoucher(txtTenCT.getText());
        vch.setTriGia(Float.parseFloat(txtTriGia.getText()));
        vch.setGiaToiThieu(Float.parseFloat(txtToiThieu.getText()));
        vch.setNgayBD(sdf.parse(ngayBD));
        vch.setNgayKT(sdf.parse(ngayKT));
        vch.setGhiChu(tarGhiChu.getText());
    }

    public void clear() {
        txtMaGiamGia.setText("");
        txtTenCT.setText("");
        txtTriGia.setText("");
        txtToiThieu.setText("");
        jdcNgayBD.setDate(null);
        jdcNgayKT.setDate(null);
        tarGhiChu.setText("");
        txtMaGiamGia.enable(true);
    }

    public void setTextForm(Voucher vch) {
        txtMaGiamGia.setText(vch.getMaVoucher());
        txtTenCT.setText(vch.getTenVoucher());
        txtTriGia.setText(String.valueOf(vch.getTriGia()));
        txtToiThieu.setText(String.valueOf(vch.getGiaToiThieu()));
        jdcNgayBD.setDate(vch.getNgayBD());
        jdcNgayKT.setDate(vch.getNgayKT());
        tarGhiChu.setText(vch.getGhiChu());
    }
