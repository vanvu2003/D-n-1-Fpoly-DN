    private void btnTaoHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoHDActionPerformed
        String nameKH = JOptionPane.showInputDialog(this, "Nhập tên khách hàng");
        
        if(nameKH == null){
            return;
        }
            HoaDon hd = new HoaDon();
            HoaDonDAO hdDao = new HoaDonDAO();
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            Date date = new Date();
            
            Random generator = new Random();

            int maHD = generator.nextInt(10000);
            String ngayTao = df.format(date);
            

            lbMaHD.setText(String.valueOf(maHD));
            lbTenKH.setText(nameKH);
            lbMaNV.setText(ShareHelper.name);
            lbNgayTao.setText(ngayTao);
            pnlBill.setVisible(true);
            btnXoaSPMua.setVisible(false);
            btnThemHDCho.setVisible(true);
            lbTienVoucher.setVisible(false);
            try {
                setHoaDon(hd, df.parse(ngayTao));
            } catch (ParseException ex) {
                Logger.getLogger(JPanelBanHang.class.getName()).log(Level.SEVERE, null, ex);
            }

            hdDao.insert(hd);
    }//GEN-LAST:event_btnTaoHDActionPerformed

    private void cbxLoaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxLoaiActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        SanPhamDAO spDao = new SanPhamDAO();
        ArrayList<SanPham> listSP = new ArrayList<>();

        int loai = 0;
        if (cbxLoai.getSelectedIndex() == 1) {
            loai = 1;
        } else if (cbxLoai.getSelectedIndex() == 2) {
            loai = 2;
        } else if (cbxLoai.getSelectedIndex() == 0) {
            listSP = (ArrayList<SanPham>) spDao.fillAll();
            model.setRowCount(0);

            for (SanPham sp : listSP) {
                model.addRow(new Object[]{sp.getMaSP(), sp.getTenSP(), sp.getGia(), sp.getHinh(), sp.getGhiChu(), sp.getMaLoai() == 1 ? "Đồ ăn vặt" : "Nước giải khác"});

            }
            tblSanPham.setModel(model);
            return;
        }

        listSP = (ArrayList<SanPham>) spDao.findByTenLoai(loai);
        model.setRowCount(0);

        for (SanPham sp : listSP) {
            model.addRow(new Object[]{sp.getMaSP(), sp.getTenSP(), sp.getGia(), sp.getHinh(), sp.getGhiChu(), sp.getMaLoai() == 1 ? "Đồ ăn vặt" : "Nước giải khác"});

        }
        tblSanPham.setModel(model);

    }//GEN-LAST:event_cbxLoaiActionPerformed

    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked

        if (!lbMaHD.getText().equals("")) {
            CTHoaDon cthd = new CTHoaDon();
            int row = tblSanPham.getSelectedRow();
            String soLuong = JOptionPane.showInputDialog(this, "Nhập số lượng cần mua");
            if(soLuong == null){
                return;
            }
            DefaultTableModel model = (DefaultTableModel) tblSPMua.getModel();

            String id = (String) tblSanPham.getValueAt(row, 0);

            SanPhamDAO spDao = new SanPhamDAO();
            SanPham sp = spDao.findByID(id);

            setCTHD(cthd, id, soLuong, sp);

            float tongTienHang = Integer.parseInt(soLuong) * sp.getGia();
            sum = sum + tongTienHang;

            lbTienHang.setText(String.valueOf(sum));
            model.addRow(new Object[]{sp.getTenSP(), sp.getGia(), soLuong, tongTienHang});
            tblSPMua.setModel(model);
            
            float tongTien = sum + Float.parseFloat(lbTienVoucher.getText());
            lbTongtien.setText(String.valueOf(tongTien));
        }

    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void txtTienKhachTraFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtTienKhachTraFocusLost
        if(txtTienKhachTra.getText().equals("")){
            return;
        }

        if (!lbTongtien.getText().equals("")) {
            
            float tienDu = Float.parseFloat(txtTienKhachTra.getText()) - Float.parseFloat(lbTongtien.getText());

            if (tienDu < 0) {
                JOptionPane.showMessageDialog(this, "Số tiền không đủ để thanh toán", "Thông báo", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            lbTienDu.setText(String.valueOf(tienDu));
        }else{
            return;
        }

    }//GEN-LAST:event_txtTienKhachTraFocusLost

