    private void cbxVoucherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxVoucherActionPerformed
        String maVoucher = (String) cbxVoucher.getSelectedItem();
        if(cbxVoucher.getSelectedIndex() == 0){
            lbTienVoucher.setText("0");
            return;
        }
        VoucherDAO vchDao = new VoucherDAO();
        
        Voucher vch = vchDao.findByMaVoucher(maVoucher);
        
        if(Float.parseFloat(lbTienHang.getText()) < vch.getGiaToiThieu()){
            JOptionPane.showMessageDialog(this, "Giá trị đơn hàng của bạn chưa đạt mức tối thiếu khuyến mãi!!");
            return;
        }
        lbTienVoucher.setText("-"+String.valueOf(vch.getTriGia()));
        Float tongTien = Float.parseFloat(lbTienHang.getText()) + Float.parseFloat(lbTienVoucher.getText());
        lbTongtien.setText(String.valueOf(tongTien));
        lbTienVoucher.setVisible(true);
    }//GEN-LAST:event_cbxVoucherActionPerformed

